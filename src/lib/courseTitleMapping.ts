export const mapBengaliCourseTitle = (title: string): string => {
  if (!title) return 'Course';
  
  // First try exact match
  if (courseTitleMapping[title]) {
    return courseTitleMapping[title];
  }
  
  // Then try partial matches
  let mappedTitle = title;
  Object.entries(courseTitleMapping).forEach(([bengali, english]) => {
    if (title.includes(bengali)) {
      mappedTitle = mappedTitle.replace(new RegExp(bengali, 'g'), english);
    }
  });
  
  // Clean up and return
  mappedTitle = mappedTitle
    .replace(/[\u0980-\u09FF]/g, '') // Remove remaining Bengali characters
    .replace(/\s+/g, ' ') // Clean up spaces
    .trim();
  
  // If nothing remains after cleaning, return a fallback
  return mappedTitle || 'CAD Training Course';
};
